package braceForce.distribution;

public class BundlePack {
	private String bundleKey;
	private Object bundleValue;
	public String getBundleKey() {
		return bundleKey;
	}
	public void setBundleKey(String bundleKey) {
		this.bundleKey = bundleKey;
	}
	public Object getBundleValue() {
		return bundleValue;
	}
	public void setBundleValue(Object bundleValue) {
		this.bundleValue = bundleValue;
	}
}
